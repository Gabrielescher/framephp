                    <h1>O que é Framework Laravel?</h1>
                    <h3>Ele permite aos desenvolvedores especificar e instalar bibliotecas e pacotes de terceiros em seus projetos.<br>
                                  Isso simplifica o processo de gerenciamento de dependências, facilitando a inclusão e atualização de bibliotecas externas necessárias para o desenvolvimento de aplicações Laravel.</h3>
<p>
                     <h1>Como é usado?</h1>
                     <h3>Laravel é uma ferramenta poderosa para desenvolvedores PHP que buscam construir aplicações web modernas e eficientes.<br> Sua combinação de simplicidade, robustez e uma comunidade ativa torna-o uma excelente escolha para projetos de todos os tipos.</h3>
</p>
                      <h1>O motivo de o usarem?</h1>
                      <h3>O Laravel é ideal para construir todo tipo de aplicativos web. <br>Funciona sites institucionais, blogs, plataformas de e-commerce e sistemas de gerenciamento de conteúdo (CMS).<br> Ele fornece uma estrutura robusta para lidar com autenticação, manipulação de bancos de dados, gerenciamento de sessões e muito mais</h3>
<p>
                <h1>Quando foi criado?</h1>
                <h3>Como Taylor B. Otwell era um grande fã do CodeIgniter, ele aproveitou a oportunidade para criar um framework mais moderno que teria as características do CodeIgniter. <br>Sendo assim, em 2011 ele criou o Laravel.</h3>
</p>


